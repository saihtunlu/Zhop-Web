export default {
  data() {
    return {
      email_message: "",
      loading: false,
      email_error: false,
      success: false,
      email: "",
      password: "",
      signUp: false,
      email_field: false,
      password_field: false,
      remember_me: {},
      remember: false,
      errorMessage: {},
      error: false,
      activePrompt: false,
      data: {
        email: "",
        password: "",
        errorMessage: "",
        password_confirmation: "",
        username: "",
        password_confirmation_field: false,
        email_field: false,
        password_field: false,
        passwordNotMatch: false,
      },
    };
  },
  created() {
    this.$emit("showTab", false);
  },
  mounted() {
    var check = JSON.parse(localStorage.getItem("remember_me"));
    if (check) {
      this.email = check.email;
      const decryptedText = this.CryptoJS.AES.decrypt(
        check.password,
        "Secret Passphrase"
      ).toString(this.CryptoJS.enc.Utf8);
      this.password = decryptedText;
      this.remember = true;
    } else {
      this.email = "";
      this.password = "";
      this.remember = false;
    }
  },
  methods: {
    checkPassword() {
      if (this.data.password !== this.data.password_confirmation) {
        this.error = true;
        this.errorMessage = "Password confirmation does not match.";
        return false;
      } else {
        this.error = false;
        return true;
      }
    },
    login() {
      if (!this.email) {
        this.email_field = true;
        return false;
      }
      if (!this.password) {
        this.password_field = true;
        return false;
      }
      this.loading = true;
      if (this.remember) {
        this.remember_me.email = this.email;
        const encryptedPassword = this.CryptoJS.AES.encrypt(
          this.password,
          "Secret Passphrase"
        ).toString();
        this.remember_me.password = encryptedPassword;
        let parsed = JSON.stringify(this.remember_me);
        localStorage.setItem("remember_me", parsed);
      } else {
        localStorage.removeItem("remember_me");
      }
      this.$store
        .dispatch("login", {
          email: this.email,
          password: this.password,
          remember: this.remember_me,
        })
        .catch((err) => {
          this.loading = false;
          this.error = true;
          this.errorMessage = err.response.data.error;
        });
    },
    register() {
      if (!this.data.email) {
        this.data.email_field = true;
        return false;
      }
      if (!this.data.username_field) {
        this.data.username_field = true;
        return false;
      }
      if (!this.data.password) {
        this.data.password_field = true;
        return false;
      }
      if (!this.data.password_confirmation) {
        this.data.password_confirmation_field = true;
        return false;
      }
      var data = this.data;
      this.loading = true;
      this.$store
        .dispatch("register", {
          data,
        })
        .then(() => {
          this.loading = false;
        })
        .catch((err) => {
          this.loading = false;
          this.error = true;
          this.errorMessage = err.response.data.message;
        });
    },
  },
};
